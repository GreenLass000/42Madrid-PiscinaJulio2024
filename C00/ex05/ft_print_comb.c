#include <unistd.h>

void	ft_print_comb(void)
{
	int	n1;
	int	n2;
	int	n3;

	n1 = '0';
	n2 = '1';
	n3 = '2';
	while (n1 <= '7')
	{
		while (n2 <= '8')
		{
			while (n3 <= '9')
			{
				write(1, &n1, 1);
				write(1, &n2, 1);
				write(1, &n3, 1);
				if (n1 != '7')
					write(1, ", ", 2);
				n3++;
			}
			n3 = ++n2 + 1;
		}
		n2 = ++n1;
	}
}
